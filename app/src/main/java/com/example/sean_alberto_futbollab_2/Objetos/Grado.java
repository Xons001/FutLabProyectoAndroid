package com.example.sean_alberto_futbollab_2.Objetos;

public class Grado {

    private String grado_id;
    private String gradoDescripcion;
    private String gradoHorasTotales;
    private String gradoFechaInicio;
    private String gradoPrecio;

    public String getGrado_id() {
        return grado_id;
    }

    public void setGrado_id(String grado_id) {
        this.grado_id = grado_id;
    }

    public String getGradoDescripcion() {
        return gradoDescripcion;
    }

    public void setGradoDescripcion(String gradoDescripcion) {
        this.gradoDescripcion = gradoDescripcion;
    }

    public String getGradoHorasTotales() {
        return gradoHorasTotales;
    }

    public void setGradoHorasTotales(String gradoHorasTotales) {
        this.gradoHorasTotales = gradoHorasTotales;
    }

    public String getGradoFechaInicio() {
        return gradoFechaInicio;
    }

    public void setGradoFechaInicio(String gradoFechaInicio) {
        this.gradoFechaInicio = gradoFechaInicio;
    }

    public String getGradoPrecio() {
        return gradoPrecio;
    }

    public void setGradoPrecio(String gradoPrecio) {
        this.gradoPrecio = gradoPrecio;
    }

}