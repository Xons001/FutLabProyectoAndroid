package com.example.sean_alberto_futbollab_2.Objetos;

public class Master {

    private String master_id;
    private String masterDescripcion;
    private String masterHorasTotales;
    private String masterFechaInicio;
    private String masterPrecio;

    public String getMaster_id() {
        return master_id;
    }

    public void setMaster_id(String master_id) {
        this.master_id = master_id;
    }

    public String getMasterDescripcion() {
        return masterDescripcion;
    }

    public void setMasterDescripcion(String masterDescripcion) {
        this.masterDescripcion = masterDescripcion;
    }

    public String getMasterHorasTotales() {
        return masterHorasTotales;
    }

    public void setMasterHorasTotales(String masterHorasTotales) {
        this.masterHorasTotales = masterHorasTotales;
    }

    public String getMasterFechaInicio() {
        return masterFechaInicio;
    }

    public void setMasterFechaInicio(String masterFechaInicio) {
        this.masterFechaInicio = masterFechaInicio;
    }

    public String getMasterPrecio() {
        return masterPrecio;
    }

    public void setMasterPrecio(String masterPrecio) {
        this.masterPrecio = masterPrecio;
    }

}