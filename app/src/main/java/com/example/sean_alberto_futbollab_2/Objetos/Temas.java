package com.example.sean_alberto_futbollab_2.Objetos;

public class Temas {

    private String tema_id;
    private String nombre_tema;
    private String master_id;

    public String getTema_id() {
        return tema_id;
    }

    public void setTema_id(String tema_id) {
        this.tema_id = tema_id;
    }

    public String getNombre_tema() {
        return nombre_tema;
    }

    public void setNombre_tema(String nombre_tema) {
        this.nombre_tema = nombre_tema;
    }

    public String getMaster_id() {
        return master_id;
    }

    public void setMaster_id(String master_id) {
        this.master_id = master_id;
    }

}
