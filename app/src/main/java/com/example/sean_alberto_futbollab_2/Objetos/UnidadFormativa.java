package com.example.sean_alberto_futbollab_2.Objetos;

public class UnidadFormativa {

    private String uf_id;
    private String nombre_uf;
    private String grado_id;

    public String getUf_id() {
        return uf_id;
    }

    public void setUf_id(String uf_id) {
        this.uf_id = uf_id;
    }

    public String getNombre_uf() {
        return nombre_uf;
    }

    public void setNombre_uf(String nombre_uf) {
        this.nombre_uf = nombre_uf;
    }

    public String getGrado_id() {
        return grado_id;
    }

    public void setGrado_id(String grado_id) {
        this.grado_id = grado_id;
    }

}
